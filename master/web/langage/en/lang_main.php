<p>Is it possible to have fun doing maths? The SMAC team says &laquo;yes&raquo;!
Math en Jeu is a new interactive multimedia game developped by SMAC and accessible
for free on the internet. The goal, in line with SMAC\'s mission, is to expose youth to 			
mathematics through play, and to invite the general public to renew with mathematics.</p>	

<h2>How does it work?</h2>

<p>To play, nothing is simpler:</p>

<ul>
<li>Create your own identity;</li>
<li>launch a new game or join a game in progress;</li>
<li>and it\'s started!</li>
</ul>


<p>Math en Jeu is basically a board game with a mathematical flavour.
Up to four players confront each other in a game by moving on a randomly 
created board. The players try to accumulate as many coins as possible before
time runs out. To be able to win coins, each player must answer mathematical 
questions. The longer the displacement, the harder the question, and the more 
coins it is worth! </p>

<a target="_blank" href="img/screen2_big.png"><img src="img/screen2.png" alt="Capture d\'�cran" border="0" hspace="15" align="right"></a>    


<p>In case a player answers a question  wrongly, a retroactive effect
unique to each question is shown. This retroactive effect explains to the player
the reason of his mistake, and how to obtain the right answer. In order to make
the game more dynamic, all the players in a same game can move simultaneously, and 
independently. It is also possible to play alone by asking the computer to supply
three virtual opponents.</p>


<h2>Question bank</h2>


<p>The content of Math en Jeu is based on a question bank of 1500 questions
divided into 8 categories (geometry, logic, algebra, arithmetics, probability 
and statistics, history of science, functions, and general questions.) Each 
question corresponds to a specific objective of the mathematics program of Qu�bec\'s 
Minist�re de l\'�ducation, du loisir et du sport, and has points that corresponds to
it\'s degree of difficulty relative to the academic level of the player. It is 
thus possible for two players of different academic levels to confront each other
in a same game because they are given questions adapted to their levels. Every 
academic level is reprensented, with a concentration on high school mathematics.</p>



<p>Math en jeu is a constantly evolving project. In the following months,
the game will be enriched with more questions on elementary, and middle school
mathematics. At the request of the teachers, it will also be possible to use 
Math en jeu to do homeworks from home. The teacher will be able to isolate a question 
category (for example, geometry), or to add his own questions to the game. </p>


<p>
Math and jeu can be launched from any computer connected to the internet
without any special installation. The source code is entirely open (open source), 
and freely available. Thanks to the modulary architecture of the software,
it will eventually be usable for other sciences. Thus, "Physique en jeu",
"Chimie en jeu", and so on will eventually be developped.
</p>


<p>Enough said, you can now have fun&hellip; with Math en jeu!</p>';




